<h4>Présentation</h4>
<p>Se déplaçant le long de l'axe Thionville-Metz, le Pizz'Truck vous propose des pizzas artisanales à emporter. Avec un savoir-faire unique, notre pizzaiolo prépare de délicieuses pizzas dans le plus grand respect de la tradition italienne. Les préparations traditionnelles offrent les saveurs les plus authentiques des recettes italiennes, pour cela notre pâte à pizza est pétrie chaque jour sur place dans nos pétrins et façonnée à la main notre pizzaiolo. Nos pizzas sont ensuite garnies des meilleurs ingrédients et cuites à la commande dans nos cuisines ouvertes, sous les yeux de nos clients.
Chez Pizz' Truck, nous vous proposons tout au long de l’année de nouvelles recettes salées et sucrées, pour que chaque repas soit l’occasion d’une dégustation inédite.
Pour vous apporter davantage de plaisir et de gourmandise, nous mettons régulièrement en avant des produits originaux et délicieux dans nos nouvelles recettes. Chacune de ces nouveautés sont concoctées par notre chef, testées et optimisées pour répondre au mieux à vos attentes.</p>

